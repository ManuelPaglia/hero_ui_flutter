/// Colore semantico del fallback di [HUFAvatar] (iniziali o icona).
///
/// Allineato ai token HeroUI: `default` → [HUFThemeColors.secondary],
/// `accent` → [HUFThemeColors.primary], ecc.
enum HUFAvatarColor {
  /// Sfondo neutro con accento primario.
  defaultColor,

  /// Accento primario del tema.
  accent,

  success,
  warning,
  danger,
}
